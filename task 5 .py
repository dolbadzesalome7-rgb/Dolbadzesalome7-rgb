import sys

# ვკითხულობთ შეყვანას სტანდარტული ნაკადიდან (sys.stdin).
# ეს მეთოდი მუშაობს კონკურსების უმეტეს გარემოში.
try:
    # კითხულობს მთელ სტრიქონს, ყოფს სივრცეებით და გარდაქმნის რიცხვებად
    a, b = map(int, sys.stdin.read().split())
except:
    # თუ შეყვანა არასწორია ან არ არსებობს, პროგრამა გამოდის.
    sys.exit()

# გამოთვლა:
# (a AND b) + (a OR b) + (a XOR b) = 2 * (a OR b)
result = 2 * (a | b)

# შედეგის დაბეჭდვა
print(result)
