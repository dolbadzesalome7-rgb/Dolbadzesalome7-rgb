# შეყვანა (Input)
# W - საზამთროს წონა
try:
    W = int(input("შეიყვანეთ საზამთროს წონა W (1 <= W <= 100): "))
except ValueError:
    print("არასწორი შეყვანა. გთხოვთ შეიყვანოთ მთელი რიცხვი.")
    exit()

# ლოგიკა
# 1. წონა უნდა იყოს ლუწი (W % 2 == 0)
# 2. წონა უნდა იყოს 2-ზე მეტი (W > 2) (რადგან 2-ს ვერ გაყოფ 2 ლუწ დადებით რიცხვად)

if W > 2 and W % 2 == 0:
    # გამოსავალი (Output)
    print("YES")
else:
    # გამოსავალი (Output)
    print("NO")
